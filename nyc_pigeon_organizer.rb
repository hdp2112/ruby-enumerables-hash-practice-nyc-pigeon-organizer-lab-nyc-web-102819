def nyc_pigeon_organizer(data)
  new_data = {}
  data.each { |theme, value|
    new_data[value.values[0]] = {
      color: theme.keys[0][:color],
      gender: theme.keys[0][:gender],
      lives: theme.keys[0][:lives]
    }
  }
  new_data
end